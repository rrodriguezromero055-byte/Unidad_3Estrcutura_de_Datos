    /**
     * Es el evento del boton push
     * @param evt permite agragr un nuevo elemto al tope de pila
     */
    private void BtnPushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPushActionPerformed
     String valor = TxtElementos.getText().trim();
    if (valor.isEmpty()) {
        LblEstado.setText("Ingresa un valor antes de hacer Push.");
        return;
    }

    boolean ok = pila.push(valor);
    if (ok) {
        LblEstado.setText("Elemento agregado: " + valor);
        TxtElementos.setText("");
        mostrarPila();
    } else {
        LblEstado.setText("La pila está llena.");
    }
    }//GEN-LAST:event_BtnPushActionPerformed
    /**
     * Evenot del boton pop
     * @param evt este extrae el elemto del tope de al pila, si exite y lo muetra 
     */
    private void BtnPopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPopActionPerformed
     String valor = pila.pop();
    if (valor.isEmpty()) {
        LblEstado.setText("La pila está vacía.");
    } else {
        LblEstado.setText("Elemento eliminado: " + valor);
        mostrarPila();
    }
    }//GEN-LAST:event_BtnPopActionPerformed
    /**
     * Evento del bototn peek
     * @param evt muestra cual es el elemto que se encuentra actualmente en el
     * tope sin eliminarlo de la pila
     */
    private void BtnPeekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPeekActionPerformed
      String valor = pila.peek();
    if (valor.isEmpty()) {
        LblEstado.setText("La pila está vacía.");
    } else {
        LblEstado.setText("Tope actual: " + valor);
    }
    }//GEN-LAST:event_BtnPeekActionPerformed

