 /**
 * Muestra el contenido actual de la pila en el área de texto
 */
private void mostrarPila() {
    TxtArea.setText("");
    String[] datos = pila.viewStack();

    if (datos.length == 0) {
        TxtArea.setText("La pila está vacía.");
        return;
    }
    // Se muestran los elementos desde el tope hacia la base
    for (int i = 0; i < datos.length; i++) {
        TxtArea.append("[" + (datos.length - 1 - i) + "] " + datos[i] + "\n");
    }
}


