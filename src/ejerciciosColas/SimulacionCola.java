    //Este metodo actuaiza la interfaz
    private void actualizarCola() {
        // Mostrar los valores en los cuadros según el orden real
        String[] temp = new String[5];
        for (int i = 0; i < tamaño; i++) {
            temp[i] = cola[(frente + i) % 5];
        }

        TxtPos1.setText(temp[0] != null ? temp[0] : "");
        TxtPos2.setText(temp[1] != null ? temp[1] : "");
        TxtPos3.setText(temp[2] != null ? temp[2] : "");
        TxtPos4.setText(temp[3] != null ? temp[3] : "");
        TxtPos5.setText(temp[4] != null ? temp[4] : "");
    }

