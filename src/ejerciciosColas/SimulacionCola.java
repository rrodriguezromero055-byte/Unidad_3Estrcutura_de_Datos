    // Agrega el valor escrito en el cuadro de texto al final de la cola.
    // Si la cola está llena, muestra un mensaje de advertencia y no agrega nada.
    private void BtnOfferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnOfferActionPerformed
         String valor = TxtElemento.getText().trim();

        // Validar cuadro vacío
        if (valor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un valor para agregar.");
            return;
        }

        // Verificar si la cola está llena
        if (tamaño == 5) {
            JOptionPane.showMessageDialog(this, "La cola está llena.");
            LblEstado.setText("Estado: Cola llena");
            return;
        }

        // Insertar en la cola
        cola[fin] = valor;
        fin = (fin + 1) % 5; // avanzar circularmente
        tamaño++;

        // Actualizar interfaz
        actualizarCola();
        TxtElemento.setText("");
        LblEstado.setText("Estado: Se agregó " + valor);
    }//GEN-LAST:event_BtnOfferActionPerformed
     // Extrae (elimina) el primer elemento de la cola y lo muestra
     // en el cuadro de texto. Recorre los demás valores hacia el inicio.
     // Si la cola está vacía, muestra una advertencia y no realiza la acción.
    private void BtnPoliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPoliActionPerformed
           // Verificar si la cola está vacía
        if (tamaño == 0) {
            JOptionPane.showMessageDialog(this, "La cola está vacía.");
            LblEstado.setText("Estado: Cola vacía");
            return;
        }

        // Obtener y eliminar el primer elemento
        String valor = cola[frente];
        cola[frente] = null;
        frente = (frente + 1) % 5; // avanzar circularmente
        tamaño--;

        // Mostrar en el cuadro
        TxtElemento.setText(valor);
        actualizarCola();
        LblEstado.setText("Estado: Se extrajo " + valor);
    }//GEN-LAST:event_BtnPoliActionPerformed
    // Muestra el primer valor de la cola sin eliminarlo.
    // Lo enseña en una ventana emergente para evitar duplicados.
    // Si la cola está vacía, muestra una advertencia.
    private void BtnPeekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPeekActionPerformed
        // Verificar si está vacía
    if (tamaño == 0) {
        JOptionPane.showMessageDialog(this, "La cola está vacía.");
        LblEstado.setText("Estado: Cola vacía");
        return;
    }

    // Mostrar el primer elemento sin eliminarlo
    JOptionPane.showMessageDialog(this, "Elemento al frente: " + cola[frente]);
    LblEstado.setText("Estado: Frente → " + cola[frente]);
    }//GEN-LAST:event_BtnPeekActionPerformed

